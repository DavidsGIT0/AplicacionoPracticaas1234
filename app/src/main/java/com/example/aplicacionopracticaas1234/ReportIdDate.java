package com.example.aplicacionopracticaas1234;

public class ReportIdDate {
    String id, date;

    public ReportIdDate(String id, String date) {
        this.id = id;
        this.date = date;
    }

    public ReportIdDate() {

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
